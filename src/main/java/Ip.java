
public class Ip {
	private String ip;
	private String url;
	private int size;
	
	
	public Ip(String ip, String url, int size) {
		super();
		this.ip = ip;
		this.url = url;
		this.size = size;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	}
	
	
	

}
